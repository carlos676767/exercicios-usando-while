//Soma de números de 1 a 50:

//Inicialize uma variável de soma com o valor 0.
//Inicialize uma variável de número com o valor 1.
//Use um loop while para repetir enquanto o número for menor ou igual a 50.
//Dentro do loop, adicione o valor do número à variável de soma.
//Após adicionar à soma, atualize o valor do número para que ele aumente em 1 a cada iteração.
//Finalmente, imprima o resultado da soma.

let soma = 0 // cria uma variavel soma 
let numero = 1 // cria uma var numero que armazena 1

while(soma <= 50){ // verifica se soma e menor ou igual a 50
   soma = soma + numero //soma recebe soma + numero
   numero ++ // adiciona um a cada interacao
   
}
console.log(soma) //imprime na tela